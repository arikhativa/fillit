void	ft_striteri(char *s, void (*f)(unsigned int, char *))
{
	unsigned int	i;
	char			*p;

	if (!s || !(*s) || !f)
		return ;
	i = 0;
	p = s;
	while (p[i])
	{
		(*f)(i, &p[i]);
		i++;
	}
}
