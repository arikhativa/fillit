int		ft_isprime(unsigned int nbr)
{
	unsigned int i;

	if (nbr < 2 || nbr == 4)
		return (0);
	if (nbr == 2)
		return (1);
	i = 2;
	while (nbr / 2 > i && i < 65540)
	{
		if (nbr % i == 0)
			return (0);
		i++;
	}
	return (1);
}
