int		ft_is_prime(int nb)
{
	int a;
	int limit;

	limit = 46340;
	a = 2;
	if (nb < 2)
		return (0);
	if (nb == 2)
		return (1);
	while (a <= limit && a < nb / 2)
	{
		if (nb % a == 0)
			return (0);
		else
			a++;
	}
	return (1);
}
