int		ft_find_next_prime(int nb)
{
	int a;
	int limit;

	a = 2;
	limit = 46340;
	if (nb < 3)
		return (2);
	while (a < limit && a <= (nb / 2))
	{
		if ((nb % a) == 0)
		{
			nb++;
			a = 1;
		}
		a++;
	}
	return (nb);
}
