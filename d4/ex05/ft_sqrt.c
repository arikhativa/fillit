int		ft_sqrt(int nb)
{
	int a;

	if (nb < 0 || nb > 2147395600)
		return (0);
	a = 1;
	while (a * a != nb && a < nb / 2)
	{
		a++;
	}
	if (a * a == nb)
		return (a);
	else
		return (0);
}
