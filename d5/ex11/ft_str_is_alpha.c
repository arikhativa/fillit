int		ft_str_is_alpha(char *str)
{
	int n;

	n = 0;
	while (str[n] != '\0')
	{
		if (str[n] < 65)
			return (0);
		if (str[n] > 90 && str[n] < 97)
			return (0);
		if (str[n] > 122)
			return (0);
		n++;
	}
	return (1);
}
